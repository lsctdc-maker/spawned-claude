// ---------------------------------------------------------------------------
export function getCompositionTemplate(
  sectionType: string,
  colors: { primary: string; accent: string; text: string }
): SectionComposition {
  const entry = TEMPLATE_REGISTRY[sectionType];

  if (!entry) {
    // Fallback: blank canvas
    return {
      sectionType,
      variantId: `${sectionType}-default`,
      width: 860,
      height: 900,
      background: colors.primary,
      elements: [
        {
          id: `${sectionType}-bg`,
          type: 'rect',
          x: 0, y: 0, width: 860, height: 900,
          fill: colors.primary,
          locked: true,
          name: 'Background',
        },
        {
          id: `${sectionType}-placeholder`,
          type: 'text',
          x: 60, y: 420, width: 740, height: 60,
          text: sectionType.toUpperCase(),
          fontSize: 32,
          fontWeight: 700,
          fontFamily: 'Inter, sans-serif',
          textAlign: 'center',
          color: '#ffffff',
          draggable: true,
          editable: true,
          name: 'Placeholder',
        },
      ],
    };
  }

  const rawElements = entry.factory();
  const processedElements = processElements(rawElements, colors);
  const background = replacePlaceholders(entry.background, colors);

  return {
    sectionType,
    variantId: `${sectionType}-default`,
    width: entry.width,
    height: entry.height,
    background,
    elements: processedElements,
  };
}


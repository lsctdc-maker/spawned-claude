// ---------------------------------------------------------------------------
// Placeholder replacement
// ---------------------------------------------------------------------------
function replacePlaceholders(
  value: string,
  colors: { primary: string; accent: string; text: string }
): string {
  return value
    .replace(/\{primary\}/g, colors.primary)
    .replace(/\{accent\}/g, colors.accent)
    .replace(/\{text\}/g, colors.text);
}

function processElements(
  elements: CompositionElement[],
  colors: { primary: string; accent: string; text: string }
): CompositionElement[] {
  return elements.map((el) => {
    const processed: CompositionElement = { ...el };

    const strFields: (keyof CompositionElement)[] = [
      'fill', 'stroke', 'color', 'text', 'src',
    ];
    for (const field of strFields) {
      const v = processed[field as keyof CompositionElement];
      if (typeof v === 'string') {
        (processed as unknown as Record<string, unknown>)[field] = replacePlaceholders(v, colors);
      }
    }

    if (processed.gradient) {
      processed.gradient = {
        ...processed.gradient,
        from: replacePlaceholders(processed.gradient.from, colors),
        to: replacePlaceholders(processed.gradient.to, colors),
      };
    }

    return processed;
  });
}

